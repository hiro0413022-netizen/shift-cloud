-- ===== updated_at trigger =====
do $$
declare t text;
begin
  foreach t in array array['suppliers','supplier_rules','products','product_suppliers','purchase_orders','purchase_order_items','receipts','receipt_items']
  loop
    execute format('create trigger set_updated_at before update on golfwing.%I for each row execute function app.set_updated_at()', t);
  end loop;
end $$;

-- ===== RLS（テナント分離。管理系書き込みはservice_role経由: DECISIONS #11踏襲） =====
do $$
declare t text;
begin
  foreach t in array array['suppliers','supplier_rules','products','product_suppliers','purchase_orders','purchase_order_items','receipts','receipt_items']
  loop
    execute format('alter table golfwing.%I enable row level security', t);
    execute format('create policy tenant_select on golfwing.%I for select to authenticated using (company_id = app.current_company_id())', t);
    execute format('create policy tenant_insert on golfwing.%I for insert to authenticated with check (company_id = app.current_company_id())', t);
    execute format('create policy tenant_update on golfwing.%I for update to authenticated using (company_id = app.current_company_id())', t);
  end loop;
end $$;

