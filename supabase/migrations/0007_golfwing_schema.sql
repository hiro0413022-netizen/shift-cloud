-- ===== Genesis KPI用ビュー: 月次仕入コスト =====
create view golfwing.v_monthly_purchase_cost as
select
  po.company_id,
  po.store_id,
  date_trunc('month', po.order_date)::date as month,
  s.name as supplier_name,
  count(distinct po.id) as order_count,
  sum(poi.amount) as total_amount
from golfwing.purchase_orders po
join golfwing.purchase_order_items poi on poi.purchase_order_id = po.id
join golfwing.suppliers s on s.id = po.supplier_id
where po.status <> 'draft'
group by po.company_id, po.store_id, date_trunc('month', po.order_date), s.name;

grant select on golfwing.v_monthly_purchase_cost to authenticated, service_role;
